package com.example.javaproject2.codeup;

import java.util.Scanner;

public class CodeUp1079 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while(true){
            char c = sc.next().charAt(0);
            if(c != 'q'){
                System.out.println(c);
            } else{
                System.out.println(c);
                break;
            }
        }
    }
}
